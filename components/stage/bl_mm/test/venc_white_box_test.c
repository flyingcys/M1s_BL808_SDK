static void set_jpeg_header_qtable(int quality)
{
    int scalefactor, i, tmp;
    if (quality < 1) {
        quality = 1;
    }
    if (quality > 100) {
        quality = 100; 
    }
    if (quality < 50) { 
        scalefactor = 5000 / quality;
    } else {
        scalefactor = 200 - (quality * 2);
    }
    
    for (i = 0; i < 64; i++) {
        if (quality == 100) {
            jpeg_header_yuv420_test.part_1[7 + i] = 1;
            jpeg_header_yuv420_test.part_1[7 + 64 + 5 + i] = 1;
        } else {
            tmp = (y_qtable[i] *scalefactor + 50) / 100;
            jpeg_header_yuv420_test.part_1[7 + i] = tmp > 255 ? 255 : tmp;
            tmp = (uv_qtable[i] *scalefactor + 50) / 100;
            jpeg_header_yuv420_test.part_1[7 + 64 + 5 + i] = tmp > 255 ? 255 : tmp;
        }
    }
}

